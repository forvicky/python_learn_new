# 概括字符集，元字符
# \d 数字        \D非数字
# \w 匹配包括下划线的任何单词字符。类似但不等价于“[A-Za-z0-9_]”            \W 匹配任何非单词字符。等价于“[^A-Za-z0-9_]”
# \s 匹配任何不可见字符，包括空格、制表符、换页符等等。等价于[ \f\n\r\t\v]   \S 匹配任何可见字符。等价于[^ \f\n\r\t\v]
# .  匹配除换行符\n之外其他所有字符
import re

a = 'C5C++2Python0Java2Javascript'

# \d元字符，一个元字符代表一个字符
r = re.findall('\d', a)
print(r)
